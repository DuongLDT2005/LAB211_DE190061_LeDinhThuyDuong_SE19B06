package com.mycompany.de190061_ledinhthuyduong_se19b06_lab211.week8.geographic.model;

/**
 *
 * @author DELL
 */
public class Country implements Comparable<Country>{
    protected String countryCode;
    protected String countryName;
    protected float totalArea;

    public Country() {
    }

    public Country(String countryCode, String countryName, float totalArea) {
        this.countryCode = countryCode;
        this.countryName = countryName;
        this.totalArea = totalArea;
    }

    public String getCountryCode() {
        return countryCode;
    }

    public void setCountryCode(String countryCode) {
        this.countryCode = countryCode;
    }

    public String getCountryName() {
        return countryName;
    }

    public void setCountryName(String countryName) {
        this.countryName = countryName;
    }

    public float getTotalArea() {
        return totalArea;
    }

    public void setTotalArea(float totalArea) {
        this.totalArea = totalArea;
    }

    @Override
    public String toString() {
        return String.format("%-10s %-15s %-17.2f", countryCode, countryName, totalArea);
    }

    @Override
    public int compareTo(Country o) {
        return this.countryName.compareTo(o.getCountryName());
    }
    
}
